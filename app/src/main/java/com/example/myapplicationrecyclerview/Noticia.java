package com.example.myapplicationrecyclerview;

public class Noticia {

    private String titulo;
    private String subtitulo;
    public Noticia(String tit, String subtit){
        titulo = tit;
        subtitulo = subtit;
    }
    public String getTitulo(){
        return titulo;
    }
    public String getSubtitulo(){
        return subtitulo;
    }


}
